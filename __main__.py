# Define an RTP Generator that can run autonomously as a thread
class RtpGenerator(object):

    def __init__(self, keyPressed, UDP_TX_IP, UDP_TX_PORT, txRate, payloadLength):
        # Assign instance variables
        self.UDP_TX_IP = UDP_TX_IP
        self.UDP_TX_PORT = UDP_TX_PORT
        self.txRate = txRate
        self.txPeriod = 0  # Calculated from self.txRate
        self.payloadLength = payloadLength
        self.keyPressed = keyPressed
        self.txCounter_bytes = 0
        self.txActualTxRate_bps = 0
        self.txBps_1s = 0               # Used to 'sample' the actual tx rate
        self.syncSourceIdentifier =65534
        self.rtpPayload = ""                 # The 'dummy data' sent in the packet
        self.elapsedTime = datetime.timedelta()

        # Start the generator thread
        self.rtpGeneratorThread = threading.Thread(target=self.__rtpGeneratorThread, args=())
        self.rtpGeneratorThread.daemon = True # Thread will auto shutdown when the prog ends
        self.rtpGeneratorThread.start()

    def getStats(self):
        # Returns a dictionary of useful stats
        return {'Dest IP': self.UDP_TX_IP,
                'Dest Port': self.UDP_TX_PORT,
                'Tx Rate': self.txRate,
                'Tx Rate (actual)': self.txActualTxRate_bps,
                'Packet size': self.payloadLength,
                'Bytes transmitted': self.txCounter_bytes,
                'Sync Source ID': self.syncSourceIdentifier,
                'Elapsed Time': self.elapsedTime
                }

    def generatePayload(self,length):
        # Generate random string of length 'length' to create a payload
        # Create string containing all uppercase and lowercase letters
        letters = string.ascii_letters
        # iterate over stringLength picking random letters from
        self.rtpPayload = ''.join(random.choice(letters) for i in range(self.payloadLength))

    def setSyncSourceIdentifier(self,value):
        # Sets the self self.syncSourceIdentifier value
        # This is only allowed to be 32 bits long (specified by the RTP header)
        # so mask input value for safety
        maskedValue = value & 0xFFFFFFFF
        self.syncSourceIdentifier=maskedValue

    def calculateTxPeriod(self, newTxRate_bps):
        # Calculates the required tx period for a given supplied txRate and payload length
        txPeriod = self.payloadLength * 8.0 / newTxRate_bps
        return txPeriod

    def setTxRate(self, newTxRate_bps):
        # Update instance variable
        self.txRate = newTxRate_bps
        # Calculates then set the new txPeriod for a given newTxRate_bps
        self.txPeriod = self.calculateTxPeriod(newTxRate_bps)
        # Reset txBps_1s counter
        self.txBps_1s = 0

    # define a traffic generator method that will run as a thread
    # def __rtpGenerator(keyPressed, UDP_TX_IP, UDP_TX_PORT, txRate, payloadLength):
    def __rtpGeneratorThread(self):

        # Generate payload (consisting of a random string)
        self.generatePayload(self.payloadLength)
        # Attempt to create UDP socket
        try:
            txSock = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)  # Internet, UDP
        except Exception as e:
            Message.addMessage("\x1B[31__rtpGeneratorThread() socket.socket(): Cannot create socket. Exiting\x1B[0m" + self.UDP_TX_IP + ":" + \
                               str(self.UDP_TX_PORT) + ", " + str(e))
            time.sleep(2)
            exit()

        msg = "Traffic Generator thread started. Sending to " + self.UDP_TX_IP + ":" + str(self.UDP_TX_PORT) + \
              ", txRate:" + str(self.txRate) + "bps, payloadLength:" + str(self.payloadLength)
        Message.addMessage(msg)

        rtpParams = 0b01000000
        rtpPayloadType = 0b00000000
        rtpSequenceNo = 0

        enablePacketGeneration = True
        enableJitter = False

        # Calculate tx period required to provide supplied txRate for a given stringLength
        # Note: This is an estimate because time.sleep() is inherently unreliable so we have
        # to recalculate once the generator is running by averaging over a 1 sec period
        # txPeriod = self.payloadLength * 8.0 / self.txRate
        self.txPeriod = self.calculateTxPeriod(self.txRate)

        jitterPercentage = 50
        maxDeviation = self.txPeriod * jitterPercentage / 100

        # start elapsed timer
        startTime = timer()

        while True:
            # Start an execution timer (if we know the time required to construct the packet we can deduct this from the
            # txPeriod sleep time which should, in theory, reduce the jitter of the generator
            calculationStartTime = timer()

            # Create a 32 bit timestamp (needs truncating to 32 bits before passing to struct.pack)
            # 0xFFFFFFFF is 32 '1's, so the '&' operation will throw away MSBs larger than this
            rtpTimestamp = int(datetime.datetime.now().strftime("%H%M%S%f")) & 0xFFFFFFFF

            # Construct 12 byte header
            txRtpHeader = struct.pack("!BBHLL", rtpParams, rtpPayloadType, rtpSequenceNo, rtpTimestamp,
                                      self.syncSourceIdentifier)
            MESSAGE = txRtpHeader + self.rtpPayload.encode('ascii')

            # If 'z' pressed, toggle packet generation on/off
            if self.keyPressed[0] == 'z':
                if enablePacketGeneration == True:
                    # Empty keyboard buffer
                    self.keyPressed[0] = ''
                    # Clear enable flag
                    enablePacketGeneration = False
                    Message.addMessage(" 'z' Inhibiting packet generator")
                else:
                    # Empty keyboard buffer
                    self.keyPressed[0] = ''
                    # Set enable flag
                    enablePacketGeneration = True
                    Message.addMessage(" 'z' Enabling packet generator\r")
                    # Restart the 1 second timer used for txData averaging
                    startTime = timer()
            # Spacebar will introduce a single packet loss
            # If temporaryInhibit was set, clear it
            temporaryInhibit = False
            if self.keyPressed[0] == ' ':
                # Clear keyboard buffer
                self.keyPressed[0] = ''
                temporaryInhibit = True
                Message.addMessage("[Spacebar] - Inhibit single packet")

            # If all tx flags are set then transmit the rtp packet
            if enablePacketGeneration == True and temporaryInhibit == False:
                try:
                    txSock.sendto(MESSAGE, (self.UDP_TX_IP, self.UDP_TX_PORT))
                    # Update tx bytes counter
                    self.txCounter_bytes += len(self.rtpPayload)
                    # Update tx bps data counter (*8 converts bytes to bits)
                    self.txBps_1s += len(self.rtpPayload) * 8
                except Exception as e:
                    Message.addMessage("\x1B[31m__rtpGenerator() txSock.sendto(). Exiting. \x1B[0m " + str(e))
                    time.sleep(2)
                    exit()

            if self.keyPressed[0] == 'j':
                # Turn jitter on/off by pressing 'j'
                # Clear keyboard buffer
                self.keyPressed[0] = ''
                if enableJitter == False:
                    enableJitter = True
                    Message.addMessage("[j] jitter enabled")

                else:
                    enableJitter = False
                    Message.addMessage("[j] jitter disabled")

            if self.keyPressed[0] == 'w':
                # Increase tx rate by 500kbps 'w'
                # Clear keyboard buffer
                self.keyPressed[0] = ''
                newTxRate = self.txRate + (500 * 1024)
                Message.addMessage("[w] pressed. Increasing txRate from "+
                                   str(bToMb(self.txRate))+"ps to "+
                                   str(bToMb(newTxRate))+"ps")
                # Set tx period according to new rate
                self.setTxRate(newTxRate)

            if self.keyPressed[0] == 'q':
                # Decrease tx rate by 500kbps 'w'
                # Clear keyboard buffer
                self.keyPressed[0] = ''
                newTxRate = self.txRate - (500 * 1024)
                # Set a minimum tx rate of 500kbps
                if newTxRate < 500 * 1024:
                    newTxRate = 500 * 1024
                Message.addMessage("[q] pressed. Decreasing txRate from "+
                                   str(bToMb(self.txRate))+"ps to "+
                                   str(bToMb(newTxRate))+"ps")
                # Set tx period according to new rate
                self.setTxRate(newTxRate)

            if self.keyPressed[0] == 'e':
                # Increment sync source identifier
                self.keyPressed[0] = ''
                self.setSyncSourceIdentifier(self.syncSourceIdentifier+1)

            if self.keyPressed[0] == 's':
                # Increase payload length
                self.keyPressed[0] = ''
                self.payloadLength += 10
                if self.payloadLength > 1488:
                    self.payloadLength = 1488
                    self.generatePayload(self.payloadLength)
                    Message.addMessage("[s] Payload already at max size of 1488 bytes")
                else:
                    # Regenerate new payload based on the new length
                    self.generatePayload(self.payloadLength)
                    Message.addMessage("[s] Increasing payload size to "+str(self.payloadLength))

            if self.keyPressed[0] == 'a':
                # Decrease payload length
                self.keyPressed[0] = ''
                self.payloadLength -= 10
                if self.payloadLength <20:
                    self.payloadLength = 20
                    Message.addMessage("[s] Payload already at min size of 20 bytes")
                    self.generatePayload(self.payloadLength)
                else:
                    # Regenerate new payload based on the new length
                    self.generatePayload(self.payloadLength)
                    Message.addMessage("[a] Decreasing payload size to " + str(self.payloadLength))

            ###########
            # Increment rtp sequence number for next iteration of the loop
            rtpSequenceNo += 1
            # Seq no is only a 16 bit value, so reset at max value (65535)
            if rtpSequenceNo > 65535:
                rtpSequenceNo = 0
                Message.addMessage("rtpGenerator. Seq no wrapping to zero")
            # If flag set, generate random delay centred around self.txPeriod (0.01 = 10mS period)
            if enableJitter == True:
                jitter = random.uniform(-1 * maxDeviation, maxDeviation)
            else:
                jitter = 0

            # Calculate (inevitable) error in tx rate (bps)
            # If there is an error between the desired tx speed and the actual tx speed (due to timing inaccuracies of the time.sleep() command)
            # dynamically modify the self.txPeriod to actually generate the desired rate
            # 1 second timer

            # Has 1 second elapsed?
            if (timer() - startTime) >= 1 and enablePacketGeneration == True:
                # Reset elapsed timer (for 1 second timer)
                startTime = timer()

                # Increment elapsed time counter by 1 second
                self.elapsedTime += datetime.timedelta(seconds=1)

                # Test actual tx rate (averaged over a second) against 99% of desired tx rate
                if self.txBps_1s < (0.99 * self.txRate):
                    # Data not being sent fast enough, so reduce txPeriod time
                    # Measure difference between desired bps tx rate and actual bps tx rate
                    txRateError = self.txRate - self.txBps_1s
                    # Convert the difference a fraction by which will modify txPeriod
                    errorFactor = (txRateError * 1.0 / self.txRate)
                    # Modify txPeriod to compensate for error
                    # Prevent overshoots of the desired rate, only reduce self.txPeriod by 'half' the error amount in one go
                    self.txPeriod -= self.txPeriod * (errorFactor / 2.0)
                    # Message.addMessage("Compensating for timing error - Actual txData rate too low. Desired tx rate:" +
                    #                    str(self.txRate) + ", Actual tx rate:" + str(self.txBps_1s))
                # Test for overshoots
                if self.txBps_1s > (1.05 * self.txRate):
                    # Data being sent too fast, so need to reduce
                    # Measure difference between desired bps tx rate and actual bps tx rate
                    txRateError = self.txBps_1s - self.txRate
                    # Convert the difference a fraction by which will modify txPeriod
                    errorFactor = (txRateError * 1.0 / self.txRate)
                    # Reduce by 'half' the errorFactor (per adjustment) to prevent hunting
                    self.txPeriod += self.txPeriod * (errorFactor / 2.0)
                    # Message.addMessage("Data rate too high. Reducing.)")

                # Take copy of current actual tx rate
                self.txActualTxRate_bps = self.txBps_1s
                # Clear counter
                self.txBps_1s = 0

            # The calculation time will be deducted from the sleep time, which should make the generator
            # output less jittery (because the calculation time is taken into account)
            calculationPeriod = timer() - calculationStartTime

            compensatedTxPeriod = self.txPeriod + jitter - calculationPeriod
            # Have to guard against a negative time value
            if compensatedTxPeriod > 0:
                # Sleep between packet transmission
                time.sleep(compensatedTxPeriod)
            else:
                # print "__rtpGenerator() - non-positive compensatedTxPeriod value",compensatedTxPeriod,"\r"
                pass


def __diskLoggerThread(rtpRxStreamsDict):
    # Autonomous thread to iterate over rtpRxStreamsDict and poll RtpStream eventLists for new events
    # and write them  to disk
    Message.addMessage("diskLoggerThread starting")
    filename_csv = "eventLog_" + datetime.datetime.now().strftime("%H%M%S") + ".csv"
    filename_json = "eventLog_" + datetime.datetime.now().strftime("%H%M%S") + ".json"
    lastWrittenEventNoDict = {} # Dictionary to hold the last written event no for each stream
    latestEvents = []

    # Create a file and write a header
    try:
        # Write summary file
        file_csv = open(filename_csv, "w+")
        file_csv.write("Event Log summary created by isptest. Created at: " + str(datetime.datetime.now()) +
                       "\r\n-------------------------------------------------------------------------\n")
        file_csv.close()

        # Write detailed json file
        file_json = open(filename_json, "w+")
        file_json.write("Event Log json created by isptest. Created at: " + str(datetime.datetime.now()) +
                        "\r\n-------------------------------------------------------------------------\n")
        file_json.close()
    except Exception as e:
        Message.addMessage("__diskLoggerThread " + str(e))

    while True:
        # Get dictionary of available rtpRxStreams as a list
        # This will return a list of tuples [0]= sync Source id, [1]=the actual RtpStream object
        availableRtpRxStreamList = []
        temp =[]
        # Iterate over tuples returned by items() to create a list of tuples
        for k,v in rtpRxStreamsDict.items():
            temp = [k, v]
            availableRtpRxStreamList.append(temp)

        if len(availableRtpRxStreamList) > 0:
            # Iterate over availableRtpRxStreamList looking for new events
            for currentRtpStream in availableRtpRxStreamList:

                # Attempt to access rtpStream events list
                # and create a sublist of the just the latest elements
                try:
                    allEvents = currentRtpStream[1].getRTPStreamEventList()

                    # Now check to see if there are any previously unwritten events in the allEvents list
                    # Subtract lastWrittenEventNo from most recent eventNo
                    if len(allEvents) > 0:
                        # Determine the new events for this particular stream
                        # Note, if this stream is 'brand new' the key for that stream won't exist yet, so create it
                        # and set it to a default value of 0 (because we haven't written any events yet from that RtpStream object)
                        if not currentRtpStream[0] in lastWrittenEventNoDict:
                            lastWrittenEventNoDict[currentRtpStream[0]] = 0

                        # Calculate how many new (i.e not yet written to disk) events there in are in this RtpStream object
                        newEvents = allEvents[-1].eventNo - lastWrittenEventNoDict[currentRtpStream[0]]

                        if newEvents > 0:
                            # There are outstanding events to be written
                            # Slice the latest portion of the allEvents list into a sub list
                            latestEvents = allEvents[(newEvents * -1):]
                except Exception as e:
                    Message.addMessage("__diskLoggerThread - determining new events" + str(e))

                # Confirm to see that there are some events in the list
                if len(latestEvents) > 0:
                    # Open the files for writing (a denotes 'append', + denotes read/write
                    try:
                        file_csv = open(filename_csv, "a+")
                        file_json = open(filename_json, "a+")
                        for event in latestEvents:
                            # Get the event data in csv format
                            eventString = event.getCSV()+"\n"
                            # Write the event(s) to disk
                            file_csv.write(eventString)
                            # Get a json object from the event (as a string)
                            eventAsJson = event.getJSON() + "\n"
                            file_json.write(eventAsJson)
                            lastWrittenEventNo = event.eventNo
                            # Make a note of the last written event no against this stream id key
                            lastWrittenEventNoDict[currentRtpStream[0]] = event.eventNo
                        # Close the files
                        file_csv.close()
                        file_json.close()
                        # Empty the latestEvents list
                        del latestEvents[:]
                    except Exception as e:
                        Message.addMessage("__diskLoggerThread - appending to file" + str(e))
        time.sleep(1)


